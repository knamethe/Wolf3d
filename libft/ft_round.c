double		ft_round(double x)
{
	if (x > 0 && (long)x < (long)(x + 0.5))
		return ((double)(long)(x + 1));
	if (x < 0 && (long)x > (long)(x - 0.5))
		return ((double)(long)(x - 1));
	return ((double)(long)x);
}
